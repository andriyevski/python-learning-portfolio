# Work with Slices
players = ['charles','martina','michael','eli']
print(players[:3])
## Code ## : ['charles','martina','michael']

# Or start slices from 2 index
print(players[2:])
## Code ##: ['michael','eli']

# Distinct index ( last three index )
print(players[-3:])
## Code ## : ['martina','michael','eli']
